def normalize_skill(skill: str) -> str:
    s = skill.lower().strip()
    if s in _NORMALIZATION:
        return _NORMALIZATION[s]

    # fuzzy match
    for key in _NORMALIZATION:
        if key in s:
            return _NORMALIZATION[key]

    return s  # fallback


# ---------------------------------------------------------
# GET COURSES FOR A SKILL
# ---------------------------------------------------------

def get_courses_for_skill(skill: str):
    key = normalize_skill(skill)
    return COURSES.get(key, [])


